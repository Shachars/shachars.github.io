def tokenize(pil_img):
    """
    Encode a PIL image.
    Returns:
        zq  : (1, C, 1, N)  float32  — continuous quantized latents (for decode path)
        ids : (N,)           int64   — discrete token indices (what is transmitted)
    The ids are what TCLA actually transmits — k integer IDs per frame.
    """
    x = _to_tensor(pil_img)
    with torch.no_grad():
        zq, rd = model.encode(x)
    ids = rd['min_encoding_indices'].reshape(-1).cpu().numpy().astype(np.int64)
    return zq, ids


# ── Determine decode strategy based on loaded model ─────────────────────────
# One-D-Piece (TTD-trained): supports variable-length via decode_tokens(ids[:k])
#   → decoder receives k tokens as keys/values, hallucinates the rest from priors
#   → produces paper-quality images at k=8 (see Figure 1)
# Base TiTok (no TTD): fixed-length decoder; variable-length not trained
#   → mean-fill fallback, poor quality at low k (expected behaviour)

_DECODE_STRATEGY = None   # set on first decode_prefix call

def _probe_decode_strategy():
    """Test which decode path works for the loaded model."""
    global _DECODE_STRATEGY
    try:
        # Test 1: decode_tokens with short integer sequence (preferred — matches protocol)
        dummy_ids = torch.zeros(1, max(1, N_TOKENS//4), dtype=torch.long, device=DEVICE)
        with torch.no_grad():
            out = model.decode_tokens(dummy_ids)
        assert out.shape[-1] == IMG_SIZE
        _DECODE_STRATEGY = 'decode_tokens'
        print(f'  Decode strategy: decode_tokens() — variable-length OK')
        print(f'  This is One-D-Piece (TTD-trained): paper-quality at low k expected')
    except (AssertionError, RuntimeError, Exception) as e1:
        try:
            # Test 2: decode with truncated z tensor
            dummy_z = torch.zeros(1, model.quantize.embedding.weight.shape[1],
                                   1, N_TOKENS//4, device=DEVICE)
            with torch.no_grad():
                out = model.decode(dummy_z)
            _DECODE_STRATEGY = 'decode_z'
            print(f'  Decode strategy: model.decode(z[:k]) — variable-length OK via z')
        except Exception as e2:
            _DECODE_STRATEGY = 'mean_fill'
            print(f'  Decode strategy: mean-fill fallback (base TiTok, fixed-length decoder)')
            print(f'  Quality at low k will be poor — load turing-motors/One-D-Piece-B-256')
            print(f'  for paper-quality reconstruction at 8–32 tokens')

    return _DECODE_STRATEGY


def decode_prefix(zq, k, token_ids=None):
    """
    Reconstruct image from first k tokens.

    Uses the best available decode strategy in priority order:

    1. decode_tokens(ids[:k])  — correct protocol path: transmit k integer IDs,
       receiver calls decode_tokens. If One-D-Piece, this gives TTD-quality images.

    2. model.decode(z[:,:,:,:k]) — pass k continuous embeddings, same result.

    3. mean-fill fallback — base TiTok only. Quality at low k is poor because
       TiTok was never trained with variable-length inputs (no TTD).

    For paper-quality images at k=8–32, you must load One-D-Piece (TTD-trained).
    """
    global _DECODE_STRATEGY
    if _DECODE_STRATEGY is None:
        _probe_decode_strategy()

    k = max(1, min(k, N_TOKENS))

    if _DECODE_STRATEGY == 'decode_tokens' and token_ids is not None:
        # decode_tokens expects (B, 1, k) — it does squeeze(1) internally
        # Using (1, k) fails when k=1 because squeeze(1) gives a 1D tensor
        ids_t = torch.from_numpy(
            token_ids[:k].astype(np.int64)
        ).reshape(1, 1, k).to(DEVICE)           # (1, 1, k)
        with torch.no_grad():
            recon = model.decode_tokens(ids_t)   # (1, 3, H, W)
        return _to_uint8(recon)

    if _DECODE_STRATEGY in ('decode_tokens', 'decode_z'):
        # ── Second path: continuous quantized embeddings
        try:
            z_prefix = zq[:, :, :, :k]
            with torch.no_grad():
                recon = model.decode(z_prefix)
            return _to_uint8(recon)
        except (AssertionError, RuntimeError):
            _DECODE_STRATEGY = 'mean_fill'

    # ── Fallback: mean-fill (base TiTok, not TTD-trained)
    z = zq.clone()
    if k < N_TOKENS:
        z[:, :, 0, k:] = _MEAN_EMB.view(1,-1,1).expand(1,-1,N_TOKENS-k)
    with torch.no_grad():
        recon = model.decode(z)
    return _to_uint8(recon)

