def _unpack(tok):
    """Accept either (zq, ids) tuple or a bare zq."""
    if isinstance(tok, tuple): return tok[0], tok[1]
    return tok, None

def odp_decode_regular(tok, k, token_ids=None):
    """Regular FP32 decode of the first k tokens. `tok` may be (zq, ids)."""
    if k <= 0: return None
    zq, ids = _unpack(tok)
    return decode_prefix(zq, k, token_ids=token_ids if token_ids is not None else ids)

def odp_decode_fast(tok, k, token_ids=None):
    """FP16 + compiled decode of the first k tokens (same weights). `tok` may be (zq, ids)."""
    if k <= 0: return None
    zq, ids = _unpack(tok)
    if token_ids is None: token_ids = ids
    if _DECODE_STRATEGY is None: _probe_decode_strategy()
    k = max(1, min(k, N_TOKENS))
    if _DECODE_STRATEGY == 'decode_tokens' and token_ids is not None:
        ids_t = torch.from_numpy(token_ids[:k].astype(np.int64)).reshape(1,1,k).to(DEVICE)
        if DEVICE=='cuda':
            with torch.autocast(device_type='cuda', dtype=torch.float16):
                out = _compiled_dec(ids_t)
        else:
            out = _compiled_dec(ids_t)
        return _to_uint8(out.float())
    # fallback: regular path
    return decode_prefix(zq, k, token_ids=token_ids)

# odp_encode alias to the proven tokenize() (returns (zq, ids))
def odp_encode(img):
    zq, ids = tokenize(Image.fromarray(np.asarray(img).astype('uint8')))
    return zq, ids
