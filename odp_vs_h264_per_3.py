"""## 6 — Packetization and Bernoulli drop model

Each frame's bits are split into packets; each packet is dropped i.i.d. with probability PER. **ODP** keeps the longest contiguous *prefix* of arrived token-packets. **H.264** loses the whole frame if any packet drops (entropy stream), and a lost I-frame cascades the GOP. No retransmission — a dropped packet is lost for that 40ms window.
"""

MPDU_BYTES=1500
PKT_TOKENS=32              # ODP: tokens per packet (256/32 = 8 packets/frame)
def n_mpdus(nbytes): return max(1,int(np.ceil(nbytes/MPDU_BYTES)))

def odp_prefix_after_loss(p,rng):
    npk=N_TOKENS//PKT_TOKENS
    recv=rng.random(npk)>=p
    if not recv[0]: return 0
    if recv.all(): return N_TOKENS
    return int(np.argmin(recv))*PKT_TOKENS   # contiguous prefix length

def h264_frame_ok(nbytes,p,rng):
    return bool((rng.random(n_mpdus(nbytes))>=p).all())

# latency lookup: ODP decode time depends on surviving k
def odp_latency_ms(k,variant):
    if k<=0: return FRAME_MS    # frozen: no decode, but a frame slot still elapses
    # nearest benchmarked k
    ks=sorted(DT[variant].keys()); kk=min(ks,key=lambda x:abs(x-k))
    return DT[variant][kk]['mean']
print(f'ODP packets/frame: {N_TOKENS//PKT_TOKENS} (each {PKT_TOKENS} tokens = {PKT_TOKENS*BITS_PER_TOKEN//8} B)')

