def ssim(a,b):
    a=np.asarray(a,float); b=np.asarray(b,float)
    mu_a,mu_b=a.mean(),b.mean(); va,vb=a.var(),b.var(); cov=((a-mu_a)*(b-mu_b)).mean()
    c1,c2=(0.01*255)**2,(0.03*255)**2
    return float(((2*mu_a*mu_b+c1)*(2*cov+c2))/((mu_a**2+mu_b**2+c1)*(va+vb+c2)))
def psnr(a,b):
    a=np.asarray(a,float); b=np.asarray(b,float); mse=np.mean((a-b)**2)
    return float(20*np.log10(255/np.sqrt(mse))) if mse>0 else 99.0

