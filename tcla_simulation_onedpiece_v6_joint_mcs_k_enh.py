# ── Joint simulator ───────────────────────────────────────────────────────
def sim_tcla_joint(severity, lut, k_enh, mcs_tier):
    """
    Layered TXOP — same as sim_tcla_layered but with configurable enhancement MCS.
    PPDU1: k_base tokens at MCS_low (fixed — guaranteed delivery).
    PPDU2: k_enh  tokens at mcs_tier (variable — joint optimizer selects this).
    Total latency: 2 x RTT = 16ms — always fits under TMAX.
    """
    k_enh = max(0, min(int(k_enh), K_ENH_MAX))
    mcs   = MCS_TIERS[mcs_tier]

    ch_base   = make_ge(severity, BASE_PG, BASE_PB)
    k_base_rx = int(np.sum(ch_base.tx(n_mpdus(K_BASE))) * TPM)
    lat       = RTT

    k_enh_rx = 0
    if k_enh > 0:
        ch_enh   = make_ge(severity, mcs['p_g'], mcs['p_b'])
        k_enh_rx = int(np.sum(ch_enh.tx(n_mpdus(k_enh))) * TPM)
        lat     += RTT

    k_rx   = min(k_base_rx + k_enh_rx, KTOT)
    freeze = (k_base_rx == 0)
    qoe    = lut[0] if freeze else nqoe(lut, k_rx)
    return dict(k_base_rx=k_base_rx, k_enh_rx=k_enh_rx,
                k_rx=k_rx, lat=lat, qoe=qoe, freeze=freeze)


# ── Joint optimizer ───────────────────────────────────────────────────────
class JointOptimizer:
    """
    Estimates channel delivery rate from Block ACK history (EWMA).
    Selects (MCS_tier, k_enh) = argmax E[QoE] each frame.

    Algorithm:
      1. Maintain EWMA delivery rate:
           d_hat <- alpha * (k_enh_rx/k_enh_sent) + (1-alpha) * d_hat
      2. Invert delivery rate to estimate pi_bad (fraction of time in bad state)
           d_hat = (1-pi_bad)*(1-p_g) + pi_bad*(1-p_b)
           => pi_bad = (1-p_g-d_hat) / (p_b-p_g)
      3. For every (MCS_tier, k_enh) in the 2D grid:
           exp_delivery = (1-pi_bad)*(1-p_g') + pi_bad*(1-p_b')
           exp_k_rx     = k_base_expected + k_enh * exp_delivery
           exp_QoE      = lut[exp_k_rx]
      4. Return argmax, the full surface, and update cur_mcs.

    No MLLM inference.  No new protocol messages.  Only Block ACK + lut.
    """
    K_GRID = list(range(0, K_ENH_MAX+1, 16))   # 0,16,32,...,224

    def __init__(self, alpha=0.15):
        self.alpha   = alpha
        self.d_hat   = 0.70      # initial delivery rate estimate (conservative)
        self.cur_mcs = 'MCS4'   # start at midpoint

    def update(self, k_sent, k_received):
        """Update EWMA delivery rate from Block ACK outcome."""
        if k_sent > 0:
            d_obs      = k_received / k_sent
            self.d_hat = self.alpha * d_obs + (1-self.alpha) * self.d_hat

    def _pi_bad(self, mcs_name):
        """Estimate pi_bad for given MCS tier from current d_hat."""
        m   = MCS_TIERS[mcs_name]
        den = m['p_b'] - m['p_g']
        if abs(den) < 1e-6: return 0.5
        return float(np.clip((1-m['p_g']-self.d_hat) / den, 0.0, 1.0))

    def select(self, lut):
        """
        Grid search -> (best_mcs, best_k_enh, best_expected_qoe, full_surface).
        Returns surface dict surface[mcs_name][k_enh] = expected_qoe.
        """
        best_qoe, best_mcs, best_k = -1.0, self.cur_mcs, K_ENH_MAX//2
        surface = {}
        k_base_expected = K_BASE * (1 - BASE_PB)   # ~31 tokens

        for mcs_name, mcs in MCS_TIERS.items():
            pi_bad = self._pi_bad(mcs_name)
            exp_dr = (1-pi_bad)*(1-mcs['p_g']) + pi_bad*(1-mcs['p_b'])
            surface[mcs_name] = {}
            for k in self.K_GRID:
                eq = nqoe(lut, int(k_base_expected + k*exp_dr))
                surface[mcs_name][k] = eq
                if eq > best_qoe:
                    best_qoe, best_mcs, best_k = eq, mcs_name, k

        self.cur_mcs = best_mcs
        return best_mcs, best_k, best_qoe, surface


